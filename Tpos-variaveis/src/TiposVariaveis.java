public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        //constantes sempre começam com a palavra reservada final e sempre em caps lock
        final int ANO_NASCIMENTO_IGOR = 2006;
        final int ANO_ALISTAMENTO_IGOR = 2024;

        System.out.println("O ano de nascimento do Igor é "+ANO_NASCIMENTO_IGOR+ ", logo, seu ano de alistamento no serviço militar será em "+ANO_ALISTAMENTO_IGOR);
    }
}
